<h2 id="gmail-g-suite-account"><a class="header-anchor" href="#gmail-g-suite-account">#</a> Gmail/G suite account</h2>
<ul>
<li>Click on the Gmail/G suite icon to add a Gmail/G suite mail account;</li>
<li>A new window will open where you will need to sign up to your account;</li>
<li>Click Next to go to the next steps and grant needed permissions;</li>
<li>That’s it - the account is connected!</li>
</ul>
<h2 id="other-smtp-account"><a class="header-anchor" href="#other-smtp-account">#</a> Other SMTP account</h2>
<ul>
<li>Click on SMTP icon;</li>
<li>In the provided form you’ll need to fill all fields: a sender’s name and email, both SMTP and IMAP logins, passwords, servers and ports;</li>
<li>Click on Test SMTP and Test IPAM to check if provided credentials are correct - a notification will inform you if everything is alright;</li>
<li>Enabling SSL encryption and certificate validation is optional, yet we recommend enabling them to increase the security of transactions;</li>
<li>Finally, click Save to connect the account.</li>
</ul>
