<template><h1 id="how-to-setup-a-schedule-template"><a class="header-anchor" href="#how-to-setup-a-schedule-template">#</a> How to setup a schedule template</h1>
<p>Learn how to setup different sending schedulers for campaigns</p>
<p>Before sending out a campaign you’ll need to create a schedule template for your campaign - it is a schedule defining to how many contacts your emails will be going out, how often and when. One default schedule template is already prepared for you in the account.</p>
<p><img src="@source/sending_campaigns/assets/schedule_image1.png" alt="Sending schedule template"></p>
<p>To create a new schedule template you need to navigate to <em>Templates &gt; Schedule templates</em> and click <em>+Create new</em>. In the provided form you’ll need to fill these fields:</p>
<ul>
<li>Your scheduler name;</li>
<li>Pause in minutes between sends - time period between email batches sending;</li>
<li>Send messages per day - set a limit of messages for the day. Please note that it will not be exceeded if other settings indicate sending more emails than the set limit;</li>
<li>Select the time zone;</li>
<li>Select from at what hours of the day messages will be going out;</li>
<li>Select on which days messages will be going out.</li>
</ul>
<p>Click <em>Save</em> and the scheduler is ready!</p>
<p><img src="@source/sending_campaigns/assets/schedule_image1.png" alt="Save sending schedule template"></p>
<p><strong>Note:</strong> once the schedule template is saved it cannot be edited or deleted.</p>
</template>